async function main() {
    const lines = rawData.split('\n').filter(l => l.trim().length > 0);
    console.log('Total records to evaluate:', lines.length);

    let insertedCount = 0;
    for (const line of lines) {
        const parts = line.split(',');
        const apellido = parts[0] ? parts[0].trim() : '';
        const nombre = parts.slice(1).join(',').trim() || '';

        // Ignorar líneas que estén completamente vacías
        if (!apellido && !nombre) continue;

        try {
            // Buscar si ya existe para evitar duplicados, ignorando mayúsculas/minúsculas idealmente,
            // pero usaremos el match exacto para los fines del seed:
            const existing = await prisma.doctor.findFirst({
                where: {
                    apellido: apellido,
                    nombre: nombre,
                    laboratoryId: laboratoryId
                }
            });

            if (!existing) {
                await prisma.doctor.create({
                    data: {
                        apellido,
                        nombre,
                        laboratoryId
                    }
                });
                insertedCount++;
            }
        } catch (e) {
            console.error('Error inserting', line, e.message);
        }
    }

    console.log('Successfully inserted ' + insertedCount + ' new doctors.');
}

main()
    .catch(e => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
